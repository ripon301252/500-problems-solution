// তোর এক বন্ধু তোকে বলল ক্যালকুলেটর বানাতে, কিন্তু ক্যালকুলেটরে শুধু যোগ করার অপশন থাকলে হবে না। একটা ফাংশন লিখ যেখানে দুইটা সংখ্যার পার্থক্য বের করে দেখাস। যেমন, যদি 20 আর 15 পাঠাই, তাহলে আউটপুট হবে 5।
function findDifference(num1, num2) {
  const difference = num1 - num2;
  console.log("পার্থক্য:", difference);
}
findDifference(20, 15);
