// Problem 1: Call functions in the correct order (hello, world, !)
function hello() {
  console.log("হ্যালো");
}
function world() {
  console.log("ওয়ার্ল্ড");
}
function exclamation() {
  console.log("!");
}

// Calling functions in the correct order
hello(); // Prints "হ্যালো"
world(); // Prints "ওয়ার্ল্ড"
exclamation(); // Prints "!"
