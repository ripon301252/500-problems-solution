//ধর তুই একটা shoe অবজেক্ট বানালি, যেখানে brand: "Nike", size: 42 আর price: 8000 প্রোপার্টিগুলো আছে। Object.keys() দিয়ে কীগুলা বের কর, আর Object.values() দিয়ে মানগুলো বের কর।
const shoe = {
  brand: "Nike",
  size: 42,
  price: 8000,
};
console.log(Object.keys(shoe));
console.log(Object.values(shoe));
